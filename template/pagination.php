<nav>
	<div class='d-flex align-items-center justify-content-center gap-2'>
		<ul class='pagination pagination-circle mb-0'>
			<?php

				$pagesToShow = min($totalPages, 3);

				$page = $currentPage - 1;

				if( $currentPage == 1 || $currentPage > $totalPages)
				{
					$page = $currentPage;
				}
				elseif($currentPage == $totalPages)
				{
					$page = $totalPages - ($pagesToShow - 1);
				}
				?>

				<li class="page-item <?=($currentPage == 1) ? 'disabled' : ''; ?>">
					<a class='page-link' href='http://localhost/admin/users?page=1&count=<?= $limit; ?>' >First</a>
				</li> 

				<?php
					for ($i=0; $i < $pagesToShow; $i++)
					{
						?>
							<li class='page-item <?= (($page + $i) == $currentPage) ? "active" : ""; ?>'>
								<a class='page-link' href='http://localhost/admin/users?page=<?= ($page + $i); ?>&count=<?= $limit; ?>' ><?= ($page + $i); ?></a>
							</li>
						<?php
					}
				?>

				<li class="page-item <?= ($currentPage == $totalPages) ? 'disabled' : ''; ?>">
					<a class="page-link" href='http://localhost/admin/users?page=<?= $totalPages; ?>&count=<?= $limit; ?>' >Last </a>
				</li>

		</ul>
	</div>
